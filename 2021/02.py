#!/usr/bin/env python3


import re


def parser(text) -> list:
    return [(line[0], int(line[1]),) for line in re.findall(r"(\w+) (\d+)", text)]


def read_input() -> str:
    with open(__file__, encoding="utf-8") as f:
        c = f.read()
        data = c[c.rindex("ðŸŽ…") + 1: c.rindex("â›„")].strip()
    return data


# MAIN FUNCTIONS
moves = {
    "forward": lambda p, m: (p[0] + m, p[1]),
    "up": lambda p, m: (p[0], p[1] - m),
    "down": lambda p, m: (p[0], p[1] + m),
}


def part1(cmds) -> int:
    pos = (0, 0)  # x=position, y=depth
    for c in cmds:
        pos = moves[c[0]](pos, c[1])
    return pos[0] * pos[1]


moves2 = {
    "forward": lambda p, m: (p[0] + m, p[1] + p[2] * m, p[2]),
    "up": lambda p, m: (p[0], p[1], p[2] - m),
    "down": lambda p, m: (p[0], p[1], p[2] + m),
}


def part2(cmds) -> int:
    pos = (0, 0, 0)  # x=position, y=depth, z=aim
    for c in cmds:
        pos = moves2[c[0]](pos, c[1])
    return pos[0] * pos[1]


# TEST
def test():
    # GIVEN
    given = parser("""
forward 5
down 5
forward 8
up 3
down 8
forward 2
""".strip())
    assert part1(given) == 150
    # part 2
    assert part2(given) == 900

    return True


if __name__ == '__main__':
    assert test()
    input_ = parser(read_input())
