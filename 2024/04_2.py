# MAIN
def neighbors(x, y, h, w) -> List[Tuple[int, int]]:
    return [(x + i, y + j) for i in (-1, 0, 1) for j in (-1, 0, 1)
            if (i, j) != (0, 0) and -1 < x + i < h and -1 < y + j < w]

# REWRITTEN with namedtuple and pointer instead of built-in lists of tuples
def part1(given: List[str]) -> int:
    KEYWORD = 'XMAS'
    # x,y - coordinates of the word in the letters matrix
    # size - length of the word
    # prev - pointer to previous character
    Word = collections.namedtuple('Word', 'x y size prev')

    def bfs(letters: List[str], word: Word):
        counter = 0
        x, y = word.x, word.y
        if letters[x][y] == KEYWORD[word.size - 1]:
            if word.size == len(KEYWORD):
                # identified.append(p)
                counter += 1
            else:
                for nx, ny in neighbors(x, y, len(letters), len(letters[0])):
                    if word.size < 2 or word.prev.x - x == x - nx and word.prev.y - y == y - ny:  # word must not break, i.e. have the same direction
                        # identified.extend(bfs(letters, [p + [(nx, ny)]]))
                        counter += bfs(letters, Word(nx, ny, word.size + 1, word))

        # return identified
        return counter

    height, width = len(given), len(given[0])
    # found = []
    result = 0
    for x in range(height):
        for y in range(width):
            if given[x][y] == 'X':
                # found.extend(bfs(given, [[(x, y)]]))
                result += bfs(given, Word(x, y, 1, None))
    return result


def part2(given: List[str]) -> int:
    def get_x_chars(letters, x, y):
        return ''.join(letters[i][j] for i, j in [(x - 1, y - 1), (x - 1, y + 1), (x + 1, y - 1), (x + 1, y + 1)])

    height, width = len(given), len(given[0])
    # found = []
    result = 0
    for x, line in enumerate(given):
        for y, char in enumerate(line):
            if (
                    char == 'A'  # start with A
                    and 0 < x < height - 1 and 0 < y < width - 1  # check for out of bounds, A must not be on the edge
                    and get_x_chars(given, x, y) in ('MMSS', 'MSMS', 'SSMM', 'SMSM')  # check corners of the square with A in the middle, options are as follows
                    # .M.M.      .M.S.      .S.S.      .S.M.
                    # ..A..  OR  ..A..  OR  ..A..  OR  ..A..
                    # .S.S.      .M.S.      .M.M.      .S.M.
            ):
                # found.append((x,y))
                result += 1
    return result


